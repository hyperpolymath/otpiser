/// Extract the root module name from an entry like "MyApp.Application".
/// Returns the first segment (e.g., "MyApp") or the full entry if no dot.
fn extract_root_module(entry: &str) -> &str {
    // Find the last dot-separated segment that is "Application" and strip it.
    if let Some(pos) = entry.rfind(".Application") {
        &entry[..pos]
    } else if let Some(pos) = entry.rfind('.') {
        &entry[..pos]
    } else {
        entry
    }
}

